---query to categorize and identify for the type of loan that was repaid
UPDATE LOAN_REPAYMENT
SET LOAN_ID = 
    CASE 
        WHEN TO_NUMBER(LOAN_ID) BETWEEN 100000001 AND 100000050 THEN CONCAT('S', LPAD(SUBSTR(TO_CHAR(LOAN_ID), 2), 9, '0')) -- Student loans
        WHEN TO_NUMBER(LOAN_ID) BETWEEN 100000051 AND 100000100 THEN CONCAT('B', LPAD(SUBSTR(TO_CHAR(LOAN_ID), 2), 9, '0')) -- Business loans
        WHEN TO_NUMBER(LOAN_ID) BETWEEN 100000101 AND 100000154 THEN CONCAT('P', LPAD(SUBSTR(TO_CHAR(LOAN_ID), 2), 9, '0')) -- Personal loans
        ELSE TO_CHAR(LOAN_ID) -- Leave unchanged if not in the specified ranges
    END
WHERE REGEXP_LIKE(LOAN_ID, '^[0-9]+$');  -- Ensures only numeric LOAN_IDs are updated